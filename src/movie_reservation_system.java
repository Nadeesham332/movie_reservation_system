    /**
     * Check seats except last line are selected or not
     * @param checkingSeat : currently checking button
     * @param line : currently checking seat line
     */
    public void normalSeats(javax.swing.JButton checkingSeat,String line){
        if(checkingSeat.getBackground().equals(new Color(153,153,153))){
            checkingSeat.setBackground(Color.GREEN);
            this.Total+=12;
            this.Ticket+=1;
            this.reservedSeats=this.reservedSeats+line+"-"+checkingSeat.getText()+"    ";
            showUpdates();
        
        }
        
        else if(checkingSeat.getBackground().equals(Color.GREEN)){
            checkingSeat.setBackground(new Color(153,153,153));
            this.Total-=12;
            this.Ticket-=1;
            this.reservedSeats=this.reservedSeats.replaceAll(line+"-"+checkingSeat.getText()+"    " , "");
            
            showUpdates();
        
        }
    }
    
    /**
     * Check last line seats are selected or not
     * @param checkingSeat : currently checking button
     * @param line : currently checking seat line
     */
    public void specialSeat(javax.swing.JButton checkingSeat,String line){
        if(checkingSeat.getBackground().equals(new Color(153,153,153))){
            checkingSeat.setBackground(Color.GREEN);
            this.Total+=20;
            this.Ticket+=1;
            this.reservedSeats=this.reservedSeats+line+"-"+checkingSeat.getText()+"    ";
            showUpdates();
    
        }
        
        else if(checkingSeat.getBackground().equals(Color.GREEN)){
            checkingSeat.setBackground(new Color(153,153,153));
            this.Total-=20;
            this.Ticket-=1;
            this.reservedSeats=this.reservedSeats.replaceAll(line+"-"+checkingSeat.getText()+"    ", "");
            showUpdates();
         
        }
    }
    
    /**
     * Make booked seats non reservable
     * @param checkingSeat : currently checking button
     */
    public void book(javax.swing.JButton checkingSeat){
        if(checkingSeat.getBackground().equals(Color.GREEN)){
            checkingSeat.setBackground(Color.RED);
            this.countReservedSeats++;
        }
    }
    
    /**
     * call message box for print the summery if the reservation was successful
     */
    public void msgBox(){
        
        //if not one of seats were selected
        if(this.Ticket==0)
            JOptionPane.showMessageDialog(null, "You need to select atleast one seat!", "Reservation Failed", 
                    JOptionPane.ERROR_MESSAGE);
        else{
            //build the message to print in message box
            String msg = "RESERVATION SUMMERY\n\nTickets: "+
                    Integer.toString(this.Ticket)+"\nToatal: $"+Integer.toString(this.Total)+"\nSeats: "+this.reservedSeats;
            JOptionPane.showMessageDialog(null, msg, "Successfully Reserved", JOptionPane.DEFAULT_OPTION);
            this.Total = 0;
            this.Ticket = 0;
            this.reservedSeats="";
            
            showUpdates();
            
            //if all seats are booked
            if(countReservedSeats==84)
                jButton1.setEnabled(false);
        }
    }
    
    /**
     * Updating results in panel
     */
    public void showUpdates(){
        jLabel21.setText(Integer.toString(this.Total));
        jLabel19.setText(Integer.toString(this.Ticket));
        jTextArea2.setText(this.reservedSeats);
    }
